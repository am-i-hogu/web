import { mockValueForTsType } from "./java-type-map.mjs";

// ----------------------
// generated mock 생성
// ----------------------
// 실제 mock 값은 사람이 다듬되, DTO shape가 바뀌었는지 빠르게 볼 수 있는 generated 초안을 만듭니다.

function constantNameForDto(tsName) {
  return tsName
    .replace(/Dto$/, "_DTO")
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .toUpperCase();
}

function renderMockValue(field) {
  const primitiveValue = mockValueForTsType(field.tsType, field.name);
  if (primitiveValue !== null) {
    return primitiveValue;
  }
  return `${constantNameForDto(field.tsType)}_MOCK`;
}

function getRecordDependencies(record, recordNames) {
  return record.fields.map((field) => field.tsType.replace(/\[\]$/, "")).filter((tsType) => recordNames.has(tsType));
}

function sortRecordsByDependencies(records) {
  const recordNames = new Set(records.map((record) => record.tsName));
  const pending = new Map(records.map((record) => [record.tsName, record]));
  const emitted = new Set();
  const sorted = [];

  while (pending.size > 0) {
    const readyRecords = [...pending.values()].filter((record) =>
      getRecordDependencies(record, recordNames).every((dependency) => emitted.has(dependency)),
    );
    const batch = readyRecords.length > 0 ? readyRecords : [...pending.values()];

    for (const record of batch) {
      sorted.push(record);
      emitted.add(record.tsName);
      pending.delete(record.tsName);
    }
  }

  return sorted;
}

export function generateMockSkeleton(records, options = {}) {
  const domain = options.domain ?? "api";
  const sortedRecords = sortRecordsByDependencies(records);
  const lines = [
    "// AUTO-GENERATED mock draft from am-i-hogu/api Java records.",
    "// Copy values into domain mock files when needed; do not edit this file manually.",
    `// Domain: ${domain}`,
    "",
    "import type {",
    ...records.map((record) => `  ${record.tsName},`),
    '} from "./post.dto";',
    "",
  ];

  for (const record of sortedRecords) {
    lines.push(`export const ${constantNameForDto(record.tsName)}_MOCK: ${record.tsName} = {`);
    for (const field of record.fields) {
      lines.push(`  ${field.name}: ${renderMockValue(field)},`);
    }
    lines.push("};", "");
  }

  return `${lines.join("\n").trimEnd()}\n`;
}
